-- 4. Activity Log Table
CREATE TABLE activity (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  action VARCHAR(255) NOT NULL,
  user_email VARCHAR(255) NOT NULL,
  ip VARCHAR(50),
  time TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 5. Contact Enquiries Table
CREATE TABLE contact_enquiries (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(255) NOT NULL,
  email VARCHAR(255) NOT NULL,
  company VARCHAR(255),
  service VARCHAR(255),
  budget VARCHAR(255),
  message TEXT NOT NULL,
  createdAt TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 6. Chat Enquiries Table
CREATE TABLE chat_enquiries (
  id UUID PRIMARY KEY,
  service VARCHAR(255),
  project_type VARCHAR(255),
  budget VARCHAR(255),
  timeline VARCHAR(255),
  contact_name VARCHAR(255),
  contact_business VARCHAR(255),
  contact_email VARCHAR(255),
  contact_phone VARCHAR(255),
  contact_requirements TEXT,
  "read" SMALLINT DEFAULT 0,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 7. Career Jobs Table
CREATE TABLE career_jobs (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  category VARCHAR(100) NOT NULL,
  title VARCHAR(200) NOT NULL,
  location VARCHAR(100) NOT NULL,
  type VARCHAR(50) NOT NULL,
  description TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  UNIQUE (category, title)
);

-- 8. Clients Table
CREATE TABLE clients (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(100) NOT NULL,
  logo VARCHAR(500),
  position INT NOT NULL DEFAULT 0,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 9. Blogs Table
CREATE TABLE blogs (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title VARCHAR(300) NOT NULL,
  slug VARCHAR(300) NOT NULL UNIQUE,
  excerpt VARCHAR(500) DEFAULT '',
  content TEXT NOT NULL,
  coverImage VARCHAR(500) DEFAULT '',
  author VARCHAR(100) NOT NULL DEFAULT 'Adway Team',
  category VARCHAR(100) DEFAULT '',
  tags VARCHAR(500) DEFAULT '',
  readingTime INT DEFAULT 1,
  published SMALLINT NOT NULL DEFAULT 0,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

